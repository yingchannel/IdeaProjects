package day04_Variable;

public class Currencies {
    public static void main(String[] args) {

        int dollar = 1000;

        double lira = dollar * 9.53 ;
        double euro = dollar * 0.94 ;
        double pound = dollar * 0.81 ;
        double jpy = dollar * 130.1 ;
        double peso = dollar * 20.4 ;
        double rmb = dollar * 6.7 ;
        double cad = dollar * 1.3 ;
        double riyal = dollar * 3.6 ;


        // jpy, pound, peso, cad, riyal

        System.out.println("lira = " + lira);
        System.out.println("euro = " + euro);
        System.out.println("pound = " + pound);
        System.out.println("jpy = " + jpy);
        System.out.println("peso = " + peso);
        System.out.println("rmb = " + rmb);
        System.out.println("cad = " + cad);
        System.out.println("riyal = " + riyal);



    }


}
